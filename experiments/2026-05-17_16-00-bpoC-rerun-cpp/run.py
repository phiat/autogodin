#!/usr/bin/env python3
"""bpoC-rerun-cpp: byte-identical recipe to bpoC-rerun-postfix but on the
upstream autogo C++ MCTS (no Odin shim).

This is the C++ side of the end-to-end strength comparison. Pair this
with the existing bpoC-rerun-postfix iter4 ckpt for a 100-game gauntlet
under matched compute. See autogodin-<TBD> parent issue.

Recipe (identical to bpoC-rerun-postfix):
  - 5,000 random×random pre_collect
  - 4 iterations of (selfplay-collect → train next iter)
  - 256ch × 10b ResNet, batch 512, 200 sims/move, 500 selfplay games/iter
  - 4 parallel selfplay workers with --batched-inference

The ONLY difference vs bpoC-rerun-postfix:
  - PYTHONPATH does NOT include python/odin_backend (so alpha_go_cpp
    resolves to the real pybind11 wheel, not the Odin shim).
  - No ALPHAGO_BACKEND env var.
  - Agent name "bpoCcpp-iter{N}" instead of "bpoC-iter{N}".
  - Pre-flight assertion that alpha_go_cpp.MCTSTree is NOT
    alpha_go_odin.MCTSTree, to catch silent shim leakage
    (per feedback_verify_backend_resolution).

Run on a JL L4 PyTorch instance after `scripts/jl_bootstrap.sh`:

  PYTHONPATH="$PWD/python:autogo/src" \\
    GAME_DATA_DIR=$HOME/nfs-local/game_data_root \\
    autogo/.venv/bin/python experiments/2026-05-17_16-00-bpoC-rerun-cpp/run.py
"""
from __future__ import annotations

import argparse
import json
import os
import shutil
import subprocess
import sys
import time
from pathlib import Path

EXP = Path(__file__).resolve().parent
EXP_NAME = EXP.name
WORKSPACE = EXP.parent.parent
AUTOGO = WORKSPACE / "autogo"
PY = AUTOGO / ".venv" / "bin" / "python"
NFS = Path(os.environ.get("NFS_ROOT", "/nfs"))


def step(title: str) -> None:
    print()
    print("=" * 70)
    print(f"=== {title}")
    print("=" * 70, flush=True)


def run(cmd: list[str], env: dict[str, str] | None = None,
        log: Path | None = None) -> float:
    print(f"$ {' '.join(cmd)}", flush=True)
    full_env = os.environ.copy()
    if env:
        full_env.update(env)
    t0 = time.time()
    if log:
        log.parent.mkdir(parents=True, exist_ok=True)
        with open(log, "wb") as f:
            r = subprocess.run(cmd, env=full_env, stdout=f, stderr=subprocess.STDOUT)
    else:
        r = subprocess.run(cmd, env=full_env)
    dt = time.time() - t0
    print(f"  [{dt:.1f}s] exit={r.returncode}", flush=True)
    if r.returncode != 0:
        if log:
            print("--- last 80 lines of log ---")
            print(log.read_text()[-6000:])
        raise SystemExit(r.returncode)
    return dt


def prepare_train_script() -> Path:
    """Copy upstream train.py and rewrite the model-size constants."""
    train_src = (AUTOGO / "experiments" / "2026-04-26_22-32-train-fromscratch"
                 / "train.py")
    local_train = EXP / "train.py"
    if local_train.exists():
        return local_train
    shutil.copy(train_src, local_train)
    text = local_train.read_text()
    # Override default arch + batch. The replacements are surgical
    # against the upstream constant declarations (line shape:
    # `MODEL_CHANNELS = 128`); if upstream changes the spelling, this
    # will fail loudly with a file-unchanged check below.
    patches = [
        ("MODEL_CHANNELS = 128",  "MODEL_CHANNELS = 256"),
        ("MODEL_N_BLOCKS = 10",   "MODEL_N_BLOCKS = 10"),  # noop, explicit
        ("MODEL_VALUE_HIDDEN = 64", "MODEL_VALUE_HIDDEN = 64"),
        ("BATCH_SIZE = 128",      "BATCH_SIZE = 512"),
        ('MODEL_NAME = "SizeInvariantGoResNet-128ch-10b"',
         'MODEL_NAME = "SizeInvariantGoResNet-256ch-10b"'),
    ]
    new_text = text
    for old, new in patches:
        if old != new and old not in new_text:
            raise RuntimeError(f"train.py upstream patch missed: {old!r}")
        new_text = new_text.replace(old, new)
    if new_text == text:
        raise RuntimeError("train.py patch made no changes")
    local_train.write_text(new_text)
    return local_train


def selfplay_collect_script(iter_n: int, ckpt: Path, num_games: int,
                            num_workers: int, mcts_sims: int,
                            save_name: str) -> Path:
    """Generate the per-iter selfplay launcher.

    Uses CppMCTSAgent + LeafBatchedNNEvaluator through the shim (which
    aliases alpha_go_cpp.MCTSTree to alpha_go_odin.MCTSTree). The 7km
    fix means the C++-style batched evaluator now works through Odin.
    """
    out = EXP / f"_collect_iter{iter_n}.py"
    out.write_text(
        f"""# auto-generated by run.py; do not edit.
import sys
from alpha_go.agents.nn_mcts import CppMCTSAgent, LeafBatchedNNEvaluator
from alpha_go.agents.base import register_agent

@register_agent("bpoCcpp-iter{iter_n}")
class _Iter{iter_n}Agent(CppMCTSAgent):
    def __init__(self):
        ev = LeafBatchedNNEvaluator("{ckpt}", 9, "256x10")
        super().__init__(
            evaluator=ev,
            num_simulations={mcts_sims},
            c_puct=1.0,
            temperature=1.0,
            add_noise=True,
            leaf_batch_size=64,
        )

from alpha_go.self_play import main as sp_main
sys.argv = [
    "self_play",
    "--black", "bpoCcpp-iter{iter_n}",
    "--white", "bpoCcpp-iter{iter_n}",
    "--board_size", "9",
    "--num_games", "{num_games}",
    "--num_workers", "{num_workers}",
    "--save-name", "{save_name}",
    "--seed", "{42 + iter_n}",
    "--batched-inference",
]
sp_main()
"""
    )
    return out


def main() -> None:
    p = argparse.ArgumentParser()
    p.add_argument("--random-games", type=int, default=5000)
    p.add_argument("--selfplay-games", type=int, default=500)
    p.add_argument("--mcts-sims", type=int, default=200)
    p.add_argument("--selfplay-workers", type=int, default=4)
    p.add_argument("--random-workers", type=int, default=8)
    p.add_argument("--num-iters", type=int, default=4,
                   help="Number of selfplay→train rounds AFTER iter0.")
    args = p.parse_args()

    assert PY.exists(), f"venv python missing: {PY}. Did jl_bootstrap.sh run?"
    assert NFS.exists(), f"{NFS} missing — bootstrap should symlink /nfs"

    logs_dir = EXP / "logs"
    logs_dir.mkdir(exist_ok=True)
    ckpt_dir = NFS / "checkpoints" / EXP_NAME
    ckpt_dir.mkdir(parents=True, exist_ok=True)

    common_pp = f"{WORKSPACE}/python:{AUTOGO}/src"
    # No odin_backend in PYTHONPATH — alpha_go_cpp resolves to the real
    # pybind11 wheel installed in autogo/.venv/site-packages. We verify
    # this below before launching any selfplay subprocess.

    # Pre-flight: confirm C++ MCTS is what we'll actually run. If the shim
    # is on PYTHONPATH for some reason, alpha_go_cpp.MCTSTree IS
    # alpha_go_odin.MCTSTree (silent alias). Catch that here so we don't
    # waste $0.55 training a "C++ baseline" that was secretly Odin again.
    # See feedback_verify_backend_resolution.
    backend_check = subprocess.run(
        [str(PY), "-c",
         "import alpha_go_cpp, alpha_go_odin; "
         "assert alpha_go_cpp.MCTSTree is not alpha_go_odin.MCTSTree, "
         "'SHIM LEAK — alpha_go_cpp is aliased to Odin'; "
         "print('OK: alpha_go_cpp ->', alpha_go_cpp.__file__)"],
        env={**os.environ, "PYTHONPATH": common_pp},
        capture_output=True, text=True,
    )
    if backend_check.returncode != 0:
        print("[backend-check FAILED]")
        print(backend_check.stdout)
        print(backend_check.stderr)
        raise SystemExit(2)
    print(f"[backend-check] {backend_check.stdout.strip()}")

    timings = {}
    iter_summaries = []

    # 1. Pre-collect random vs random (5000 games)
    random_dir = (Path(os.environ.get("GAME_DATA_DIR", "/nfs/game_data_root"))
                  / f"experiments/{EXP_NAME}/random-it0")
    if random_dir.exists() and len(list(random_dir.rglob("*.npz"))) >= args.random_games:
        print(f"[bootstrap] random-it0 already present ({args.random_games}+ npz), skipping.")
    else:
        step(f"[bootstrap] pre_collect {args.random_games} random×random games")
        random_save = f"experiments/{EXP_NAME}/random-it0"
        timings["pre_collect"] = run(
            [str(PY), "-m", "alpha_go.self_play",
             "--black", "random", "--white", "random",
             "--board_size", "9",
             "--num_games", str(args.random_games),
             "--num_workers", str(args.random_workers),
             "--save-name", random_save,
             "--seed", "0"],
            env={"PYTHONPATH": common_pp},
            log=logs_dir / "00_random.log",
        )

    # Prepare train.py (one-time patch of model size constants)
    local_train = prepare_train_script()

    # 2. Train iter0 from random data
    iter0_ckpt = ckpt_dir / "iter0_best.pt"
    if iter0_ckpt.exists():
        print(f"[iter0] iter0_best.pt already present, skipping train.")
    else:
        step("[iter0] train iter0 from random data")
        ds_it0 = EXP / "dataset-it0.txt"
        ds_it0.write_text(f"experiments/{EXP_NAME}/random-it0\n")
        timings["train_iter0"] = run(
            [str(PY), str(local_train),
             "--dataset-txt", str(ds_it0),
             "--iteration", "0",
             "--resume-from", ""],
            env={"PYTHONPATH": common_pp},
            log=logs_dir / "01_train_it0.log",
        )
        assert iter0_ckpt.exists(), f"missing {iter0_ckpt}"

    # 3..N. Iter loop: selfplay then train next iter
    prev_ckpt = iter0_ckpt
    for it in range(args.num_iters):
        next_it = it + 1
        # selfplay-it{it}
        selfplay_save = f"experiments/{EXP_NAME}/selfplay-it{it}"
        selfplay_dir = (Path(os.environ.get("GAME_DATA_DIR", "/nfs/game_data_root"))
                        / selfplay_save)
        if (selfplay_dir.exists()
            and len(list(selfplay_dir.rglob("*.npz"))) >= args.selfplay_games):
            print(f"[iter{it}] selfplay already collected, skipping.")
        else:
            step(f"[iter{it}] collect {args.selfplay_games} selfplay games "
                 f"(Odin MCTS, {args.mcts_sims} sims, "
                 f"{args.selfplay_workers} workers)")
            sp_script = selfplay_collect_script(it, prev_ckpt,
                                                 args.selfplay_games,
                                                 args.selfplay_workers,
                                                 args.mcts_sims,
                                                 selfplay_save)
            timings[f"selfplay_it{it}"] = run(
                [str(PY), str(sp_script)],
                env={"PYTHONPATH": common_pp},
                log=logs_dir / f"02_selfplay_it{it}.log",
            )

        # train iter{next_it}
        next_ckpt = ckpt_dir / f"iter{next_it}_best.pt"
        if next_ckpt.exists():
            print(f"[iter{next_it}] {next_ckpt.name} already present, skipping train.")
        else:
            step(f"[iter{next_it}] train from selfplay-it{it}, "
                 f"resume from iter{it}")
            ds = EXP / f"dataset-it{next_it}.txt"
            # Carry forward every prior data dir, drop random-it0 from
            # iter1+ (matches upstream run_iteration.sh's policy: random
            # data is the iter0 bootstrap only). Without this, iter1+
            # trains on a single selfplay batch (~50k samples) and
            # catastrophically forgets prior iters. See autogodin-8hj.
            prev_ds = EXP / f"dataset-it{it}.txt"
            kept_lines = []
            if prev_ds.exists():
                for line in prev_ds.read_text().splitlines():
                    stripped = line.strip()
                    if not stripped or stripped.startswith("#"):
                        continue
                    if "random-it0" in stripped:
                        continue  # bootstrap data is iter0-only
                    kept_lines.append(stripped)
            kept_lines.append(f"experiments/{EXP_NAME}/selfplay-it{it}")
            ds.write_text("\n".join(kept_lines) + "\n")
            timings[f"train_iter{next_it}"] = run(
                [str(PY), str(local_train),
                 "--dataset-txt", str(ds),
                 "--iteration", str(next_it),
                 "--resume-from", str(prev_ckpt)],
                env={"PYTHONPATH": common_pp},
                log=logs_dir / f"03_train_it{next_it}.log",
            )
            assert next_ckpt.exists(), f"missing {next_ckpt}"

        iter_summaries.append({"iter": next_it,
                                "trained_from": str(prev_ckpt),
                                "checkpoint": str(next_ckpt)})
        prev_ckpt = next_ckpt

    # Final summary
    summary = {
        "exp_name": EXP_NAME,
        "config": {
            "random_games": args.random_games,
            "selfplay_games_per_iter": args.selfplay_games,
            "num_iters": args.num_iters,
            "mcts_sims": args.mcts_sims,
            "selfplay_workers": args.selfplay_workers,
            "model": "SizeInvariantGoResNet-256ch-10b",
            "batch_size": 512,
        },
        "timings_seconds": timings,
        "iterations": iter_summaries,
        "checkpoints": sorted(p.name for p in ckpt_dir.glob("*.pt")),
    }
    (EXP / "summary.json").write_text(json.dumps(summary, indent=2))
    print()
    print("=" * 70)
    print("DONE. Summary written to summary.json")
    print(json.dumps(summary, indent=2))


if __name__ == "__main__":
    main()
